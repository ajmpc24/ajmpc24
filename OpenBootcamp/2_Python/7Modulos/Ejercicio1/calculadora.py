# En este ejercicio tendréis que crear un módulo que contenga las operaciones básicas de una 
# calculadora: sumar, restar, multiplicar y dividir.
  
def suma(num1, num2):
    return num1+num2

def resta(num1, num2):
    return num1-num2

def multiplicacion(num1, num2):
    return num1*num2

def division(num1, num2):
    return num1/num2
